  const _CupertinoLocalizationSoDelegate();

  @override
  bool isSupported(Locale locale) => locale.languageCode == 'so';

  @override
  Future<CupertinoLocalizations> load(Locale locale) {
    return SynchronousFuture<CupertinoLocalizations>(
      const CupertinoLocalizationSo(),
    );
  }

  @override
  bool shouldReload(_CupertinoLocalizationSoDelegate old) => false;

  @override
  String toString() => 'DefaultCupertinoLocalizations.delegate(so)';
}
