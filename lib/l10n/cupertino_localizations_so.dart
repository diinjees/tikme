  @override
  String datePickerMediumDate(DateTime date) {
    // Somali date format: "MMM d, y" - Example: "Jan 15, 2024"
    return '${_getMonthAbbr(date.month)} ${date.day}, ${date.year}';
  }

  @override
  String datePickerShortDate(DateTime date) {
    // Somali date format: "M/d/yy" - Example: "1/15/24"
    return '${date.month}/${date.day}/${date.year.toString().substring(2)}';
  }

  @override
