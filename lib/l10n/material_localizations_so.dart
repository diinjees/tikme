class _MaterialLocalizationSoDelegate
    extends LocalizationsDelegate<MaterialLocalizations> {
  const _MaterialLocalizationSoDelegate();

  @override
  bool isSupported(Locale locale) => locale.languageCode == 'so';

  @override
  Future<MaterialLocalizations> load(Locale locale) {
    return SynchronousFuture<MaterialLocalizations>(
      const MaterialLocalizationSo(),
    );
  }

  @override
  bool shouldReload(_MaterialLocalizationSoDelegate old) => false;

  @override
  String toString() => 'GlobalMaterialLocalizations.delegate(so)';
